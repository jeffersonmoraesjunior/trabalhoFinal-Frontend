function menuRedirect(site) {
    if (site === "about") {
    window.location.href = "about.html";
    }
    if (site === "index") {
    window.location.href = "index.html";
    }
    if (site === "login") {
    window.location.href = "login.html";
    }
    if (site === "formulario") {
    window.location.href = "formulario.html";
    }
}